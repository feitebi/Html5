// 区块浏览器
var baseAs = document.querySelectorAll('#container1 .baseA');
var baseAarr = [3];
var num = 3;
for (var i = 1; i < Math.floor(baseAs.length/4); i++) {
    num+=4;
    baseAarr.push(num);
};
for (var k = 0; k < baseAarr.length; k++) {
    baseAs[baseAarr[k]].style.borderRight = "1px solid #ccc";
};
$("#moveFont1").click(function(){
    $("#container1").toggleClass('overflowStyle');
})

